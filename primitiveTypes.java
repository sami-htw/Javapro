public class PrimitiveExercise {
    public static void main(String[] args) {
        

        //float and  double primitive Types


        float minfloatValue = Float.MIN_VALUE;
        float maxFloatValue = Float.MAX_VALUE;
        System.out.println("Float minimum_Value :" + minfloatValue);
        System.out.println("Float maximum_Value :" + maxFloatValue);


        Double minDoubleValue = Double.MIN_VALUE;
        Double maxDoubleValue = Double.MAX_VALUE;
        System.out.println("Double minimum_Value :" + minDoubleValue);
        System.out.println("Double maximum_Value :" + maxDoubleValue);


    }
}

